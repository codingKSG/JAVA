package ch03;

public class For05 {
	

	public static void main(String[] args) {
		
		for (int i = 0; i < 1; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
		for (int i = 0; i < 2; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();

		for (int i = 0; i < 3; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
		
		for (int i = 2; i > 0; i--) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
		for (int i = 1; i > 0; i--) {
			System.out.print("¡Ú");
		}
		System.out.println();


	}//end of main

}
