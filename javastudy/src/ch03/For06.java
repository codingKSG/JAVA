package ch03;

public class For06 {

	public static void main(String[] args) {
		for (int j = 0; j < 1; j++) {
			System.out.print(" ");
		}
		for (int i = 0; i < 1; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
		for (int j = 0; j < 0; j++) {
			System.out.print(" ");
		}
		for (int i = 0; i < 3; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
		for (int j = 0; j < 1; j++) {
			System.out.print(" ");
		}
		for (int i = 0; i < 1; i++) {
			System.out.print("¡Ú");
		}
		System.out.println();
		
	}

}
